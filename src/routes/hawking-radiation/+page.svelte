    <h1>Hawking Radiation</h1>
    <p>Hawking radiation is the theoretical thermal black-body radiation released outside
     a black hole's event horizon. This is counterintuitive because once ordinary electromagnetic
     radiation is inside the event horizon, it cannot escape. It is named after the physicist Stephen
     Hawking, who developed a theoretical argument for its existence in 1974. Hawking radiation is
     predicted to be extremely faint and is many orders of magnitude below the current best telescopes'
     detecting ability. </p>
