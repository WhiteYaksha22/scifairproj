    <h1>Detection</h1>
    <p>Black holes are detected as surrounding material like gas
        is funnelled by the force of gravity into a disk around the black hole. 
        The gas molecules in the disk swirl around the black hole so fast that they
         heat up and emit X-rays. These X-rays can be detected from Earth. 
         Black holes can also be detected by watching for motions of stars near the black hole. </p>
