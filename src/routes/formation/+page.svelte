    <h1>Formation</h1>
    <p> Primordial black holes are thought to have formed in the early universe, soon after the big bang.
        Stellar black holes form when the center of a very massive star collapses in upon itself. 
        This collapse also causes a supernova, or an exploding star, that blasts part of the star into space.
        Scientists think supermassive black holes formed at the same time as the galaxy they are in.
        The size of the supermassive black hole is related to the size and mass of the galaxy it is in.</p>
