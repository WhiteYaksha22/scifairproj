    <h1>Types of Black Holes</h1>
    <p>Astronomers generally divide black holes into three categories 
        according to their mass: stellar-mass, supermassive, and intermediate-mass.
        The mass ranges that define each group are approximate, and scientists are 
        always reassessing where the boundaries should be set. Cosmologists suspect a 
        fourth type, primordial black holes formed during the birth of the universe, 
        may also lurk undetected in the cosmos.</p>
