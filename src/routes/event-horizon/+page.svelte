    <h1>Event Horizon</h1>
    <p>In cosmology, the event horizon of the observable universe
        is the largest comoving distance from which light emitted
         now can ever reach the observer in the future. This differs
         from the concept of the particle horizon, which represents
         the largest comoving distance from which light emitted in the past
         could reach the observer at a given time. For events that occur beyond
         that distance, light has not had enough time to reach our location, even
         if it was emitted at the time the universe began. </p>
